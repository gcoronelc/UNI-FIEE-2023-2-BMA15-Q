    private void btnprocesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprocesarActionPerformed
        //Datos
        TrianguloDto dto=new TrianguloDto();
        dto.setLadoA(Double.parseDouble(txtladoA.getText()));
        dto.setLadoB(Double.parseDouble(txtladoB.getText()));
        dto.setLadoC(Double.parseDouble(txtladoC.getText()));
        //proceso
        
        ServiciosTriangulo servicio=new ServiciosTriangulo();
        
        servicio.determinavalidez(dto);
        if (dto.getValidez() == "es valido") {
            servicio.calculaPerimetro(dto);
            servicio.calculaArea(dto);
        }
        //reporte
        txtvalidez.setText(""+dto.getValidez());
        txtperimetro.setText(""+dto.getPerimetro());
        txtarea.setText(""+dto.getArea());
        
    }//GEN-LAST:event_btnprocesarActionPerformed

