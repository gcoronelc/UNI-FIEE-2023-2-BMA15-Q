public class MateService {

	/**
	 * Este servicio permite sumar 2 numeros enteros.
	 * @param num1 Parametro para el primero numero.
	 * @param num2 Parametro para el segundo numero.
	 * @return Retorna la suma de los 2 numeros.
	 */
	public int sumar(int num1, int num2) {
		return (num1 + num2);
	}

}
