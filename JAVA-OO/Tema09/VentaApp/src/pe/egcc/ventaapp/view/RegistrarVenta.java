   private void cboCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboCategoriaActionPerformed
      // Valiadaci√≥n
      if( estoyTrabajando ){
         return;
      }
      if( cboCategoria.getSelectedIndex() == -1){
         return;
      }
      // Datos
      String categoria = cboCategoria.getSelectedItem().toString();
      List<ProductoDto> productos = ventaController.getProductos( categoria );
      // Proceso
      estoyTrabajando = true;
      cboProducto.removeAllItems();
      for (ProductoDto producto : productos) {
         cboProducto.addItem(producto);
      }
      cboProducto.setSelectedIndex(-1);
      txtPrecio.setText("");
      estoyTrabajando = false;
   }//GEN-LAST:event_cboCategoriaActionPerformed

   private void cboProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboProductoActionPerformed
      // Validaci√≥n
      if( estoyTrabajando ){
         return;
      }
      if( cboProducto.getSelectedIndex() == -1 ){
         return;
      }
      // Proceso
      ProductoDto dto = (ProductoDto) cboProducto.getSelectedItem();
      txtPrecio.setText(dto.getPrecio().toString());
   }//GEN-LAST:event_cboProductoActionPerformed

   private void btnRegistrarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarVentaActionPerformed
      // Datos
      VentaDto ventaDto = new VentaDto();
      ProductoDto producto = (ProductoDto) cboProducto.getSelectedItem();
      ventaDto.setCategoria(producto.getCategoria());
      ventaDto.setProducto(producto.getProducto());
      ventaDto.setPrecio(producto.getPrecio());
      ventaDto.setCantidad(Integer.parseInt(txtCantidad.getText()));
      // Proceso
      ventaController.registrarVenta(ventaDto);
      // Limpiar el formulario
      estoyTrabajando = true;
      cboCategoria.setSelectedIndex(-1);
      cboProducto.setSelectedIndex(-1);
      txtPrecio.setText("");
      txtCantidad.setText("");
      estoyTrabajando = false;
   }//GEN-LAST:event_btnRegistrarVentaActionPerformed

